`timescale 1ns / 1ps

module imm_gen(
    input  wire [31:0] inst,        // ID阶段输入的指令
    output reg  [31:0] imm          // 生成的立即数
);

    always @(*) begin
        case (inst[6:0])
            // ---------------------------------------------------------
            // I-Type (I型指令)
            // 格式: imm[11:0] = inst[31:20]
            // ---------------------------------------------------------
            7'b0010011, // OP-IMM (ADDI, SLTI, etc.)
            7'b0000011, // LOAD   (LB, LH, LW, etc.)
            7'b1100111, // JALR
            7'b1110011: // SYSTEM (CSR / ECALL / EBREAK) - 格式同 I 型
                imm = {{20{inst[31]}}, inst[31:20]}; 

            // ---------------------------------------------------------
            // S-Type (S型指令)
            // 格式: imm[11:5] = inst[31:25], imm[4:0] = inst[11:7]
            // ---------------------------------------------------------
            7'b0100011: // STORE (SB, SH, SW)
                imm = {{20{inst[31]}}, inst[31:25], inst[11:7]}; 

            // ---------------------------------------------------------
            // B-Type (B型指令 - 分支)
            // 格式: 符号位在[31], 第11位在[7], [10:5]在[30:25], [4:1]在[11:8], 第0位恒为0
            // ---------------------------------------------------------
            7'b1100011: // BRANCH (BEQ, BNE, etc.)
                imm = {{19{inst[31]}}, inst[31], inst[7], inst[30:25], inst[11:8], 1'b0}; 

            // ---------------------------------------------------------
            // U-Type (U型指令)
            // 格式: imm[31:12] = inst[31:12], 低12位补0
            // ---------------------------------------------------------
            7'b0110111, // LUI
            7'b0010111: // AUIPC
                imm = {inst[31:12], 12'b0}; 

            // ---------------------------------------------------------
            // J-Type (J型指令 - 跳转)
            // 格式: 乱序排列, 第0位恒为0
            // ---------------------------------------------------------
            7'b1101111: // JAL
                imm = {{11{inst[31]}}, inst[31], inst[19:12], inst[20], inst[30:21], 1'b0}; 

            default:
                imm = 32'b0; 
        endcase
    end

endmodule
